import math

# градусы в радианы

math.radians(85)

x = 85  # x-в градусах
y = math.radians(x)

print(y)

y = 1.4835298641951802  # в радианах

print(round(y, 5))

y = 1.48353  # в радианах округлено до 5  символов после "."

# радианы в градусы

math.degrees(x)

x = 85  # в радианах
y = math.degrees(x)

print(y)

# y = 4870.141258611998# в градусах

print(round(y, 5))

